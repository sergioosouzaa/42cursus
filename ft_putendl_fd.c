#include <unistd.h>

void	ft_putendl_fd(char *s, int fd)
{
	size_t	pos;

	while(*(s + pos))
	{
		write(fd, *(s + pos), 1);
		pos++;
	}
	write(fd, '\0', 1);
	write(fd, '\n', 1);
}